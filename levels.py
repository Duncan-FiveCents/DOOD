class Map:
    def __init__(self,GAME,LEVEL):
        self.game = GAME
        self.levelMap = LEVEL[0]
        self.metadata = LEVEL[1]
        self.worldMap = {}
        self.createMap()
    
    def createMap(self):
        for i, row in enumerate(self.levelMap):
            for j, char in enumerate(row):
                if char != "0":
                    self.worldMap[(j,i)] = char